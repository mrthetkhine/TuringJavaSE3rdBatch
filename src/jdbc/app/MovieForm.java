    Movie getMovieInput()
    {
        Movie movie = new Movie();
        String title = this.txtMovieTitle.getText();
        Long year = Long.parseLong(this.txtYear.getText());
        
        movie.setTitle(title);
        movie.setYear(year);
        
        return movie;
    }
    private void btnAddMovieActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddMovieActionPerformed
        // TODO add your handling code here:
        Movie movie = this.getMovieInput();
        Optional<Movie> insertedMovie;
        try {
            insertedMovie = App.getApp().movieService.addMovie(movie);
            if(insertedMovie.isPresent())
            {
                movie = insertedMovie.get();
                this.movies.add(movie);

                Object row[] = movieToTableRow(movie);

                DefaultTableModel model = (DefaultTableModel)this.tblMovie.getModel();
                model.addRow(row);
                
                this.clearInput();
            }
        } catch (BusinessRuleException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        
        
        
    }//GEN-LAST:event_btnAddMovieActionPerformed

    private void clearInput()
    {
        this.txtMovieTitle.setText("");
        this.txtYear.setText("");
    }
    private Object[] movieToTableRow(Movie movie) {
        Object row[] = new Object[3];
        row[0] = movie.getId();
        row[1] = movie.getTitle();
        row[2] = movie.getYear();
        return row;
    }

