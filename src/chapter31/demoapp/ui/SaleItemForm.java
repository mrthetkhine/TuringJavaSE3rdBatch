    private void cboItemsItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboItemsItemStateChanged
        // TODO add your handling code here:
        int selectedIndex = this.cboItems.getSelectedIndex();
        System.out.println("Get selected index "+selectedIndex);
        ArrayList<Item> items = App.getInstance().itemService.getAllItems();
        
        Item selectedItem = items.get(selectedIndex);
        this.txtPrice.setText(selectedItem.getPrice()+"");
        
        if(!this.txtQuantity.getText().equals(""))
        {
            this.updateTotal();
        }
    }//GEN-LAST:event_cboItemsItemStateChanged

