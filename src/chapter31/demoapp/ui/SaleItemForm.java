    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedIndex = this.tblSaleList.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel)this.tblSaleList.getModel();
        
        if(selectedIndex != -1)
        {
            float total = Float.parseFloat(model.getValueAt(selectedIndex, 3).toString());
            this.allTotal -= total;
            this.txtAllTotal.setText(this.allTotal+"");
            model.removeRow(selectedIndex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    void addSaleEntry()
    {
        String itemName = this.cboItems.getSelectedItem().toString();
        String price = this.txtPrice.getText();
        String quantity = this.txtQuantity.getText();
        float total = Float.parseFloat(this.txtTotal.getText());
        
        this.allTotal += total;
        
        DefaultTableModel model = (DefaultTableModel)this.tblSaleList.getModel();
        Object row [] = new Object[4];
        row[0] = itemName;
        row[1] = price;
        row[2] = quantity;
        row[3] = total;
        
        model.addRow(row);
        
        this.txtAllTotal.setText(allTotal+"");
        
    }
    private void updateTotal() throws NumberFormatException {
        float price = Float.parseFloat(this.txtPrice.getText());
        float quantity = Float.parseFloat(this.txtQuantity.getText());
        float total = price * quantity;
        this.txtTotal.setText( total + "" );
    }

    void initItem()
    {
        this.cboItems.removeAllItems();
        ArrayList<Item> items = App.getInstance().itemService.getAllItems();
        for(Item item : items)
        {
            this.cboItems.addItem(item.getName());
        }
    }
